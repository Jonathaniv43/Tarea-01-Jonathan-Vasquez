// Convertir de grados Celsius a Fahrenheit

console.log('Conversor de temperatura en grados Celsius a Fahrenheit');

let temp = parseInt(prompt('Ingrese la temperatura en Grados Celsius'));
let tempFahrenheit
tempFahrenheit= (temp*(9/5)) + 32

console.log(`La temperatura de ${temp}° Celsius equivale a ${tempFahrenheit}° Fahreinheit`);
