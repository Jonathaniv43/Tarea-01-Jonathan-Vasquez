// Area de un Triangulo

console.log('Area de un Triangulo');

let base = parseFloat(prompt('Ingrese el tamaño de la Base del triangulo'));
let altura = parseFloat(prompt('Ingrese la logitud de la altura del triangulo'));

let area = ((1/2)* base * altura);
console.log(`El área de el triangulo con base ${base} y altura ${altura} es igual a ${area}`);

